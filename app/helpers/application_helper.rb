module ApplicationHelper
  ActionView::Base.default_form_builder = TailwindFormBuilder

  def gettext(text)
    text
  end

  def liked?(object, user = current_user)
    return if user.blank?
    object.liked_by?(user)
  end

  def reposted?(object, user = current_user)
    return if current_user.blank?
    Repost.find_by(user: current_user, track: object).present?
  end

  def item_class(action, kind)
    if action == kind
      "bg-default bg-opacity-50 flex p-6 border-b border-subtle"
    else
      "hover:bg-subtle hover:bg-opacity-50 flex p-6 border-b border-subtle"
    end
  end

  def categories_section(slug)
    Post.where(category: Category.friendly.find(slug))
  end

  def tab_class(item)
    item ? "tab-active" : "tab-default"
  end

  def active_tab_for?(current_tab, expected)
    if current_tab == expected
      "block"
    else
      "hidden"
    end
  end

  def serialized_to_html(serialized_content)
    Dante::Renderer.new(raw: serialized_content.with_indifferent_access).render
  #rescue => e
   # Rails.logger.error(e)
   # Bugsnag.notify(e) if defined?(Bugsnag)
   # Sentry.capture_exception(e) if defined?(Sentry)
   # nil
  end

  def event_status_label(event)
    case event.state
    when "published" then t("events.published")
    else
      t("events.unpublished")
    end
  end

  def button_label(event)
    case event.state
    when "published" then t("events.unpublish")
    else
      t("events.publish")
    end
  end

  def active_tab_link?(current_tab, expected)
    if current_tab == expected
      "bg-brand-100 text-brand-700"
    else
      "text-brand-500 hover:text-brand-700"
    end
  end

